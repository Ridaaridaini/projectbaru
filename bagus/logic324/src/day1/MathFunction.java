package day1;

public class MathFunction {
    public static void main(String[] args) {
        //pangkat
        System.out.println(Math.pow(2,4));

        //absolut
        System.out.println(Math.abs(-34));

        //pembulatan
        System.out.println(Math.round(23.3));
        System.out.println(Math.round(23.6));

        //pembulatan ke atas
        System.out.println(Math.ceil(34.2));

        //pembulatan ke bawah
        System.out.println(Math.floor(34.99));


    }
}
