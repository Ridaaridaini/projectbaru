package day2;

public class PercabanganIf {
    public static void main(String[] args) {

        int a = 15;

        if (a <= 5) {
            System.out.println("a bilangan kecil");
        }

        else if (a >= 10) {
            System.out.println("a bilangan besar");

        }else{
            System.out.println(" a bilangan biasa");
        }
    }
}
