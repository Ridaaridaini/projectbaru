//Deklarasi package, menunjukan lokasi file java ini
package day1;

public class Print {

    public static void main(String[] args) {
        //print -> Hanya mencetak saja
        System.out.print("Namaku Ika");

        // "\n" -> Ganti baris
        //hanya enter
        System.out.println();

        //println -> Setelah mencetak, ganti baris
        System.out.println("Kalo namaku Dianty");

        System.out.println("Hello tanda kutip \" Saya \"");
    }

}
