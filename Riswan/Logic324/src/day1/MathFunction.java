package day1;

public class MathFunction {
    public static void main(String[] args) {
        //perpangkatan
        System.out.println((int)Math.pow(12,2));
        //nilai absolut
        System.out.println(Math.abs(-120 - 29));
        //pembulatan
        System.out.println(Math.round(2.23));
        System.out.println(Math.round(2.77));
        //pembulatan ke atas
        System.out.println(Math.ceil(2.1));
        //pembulatan ke bawah
        System.out.println(Math.floor(2.6));
    }
}
