package day3;

public class ForLoopStructure {
    public static void main(String[] args) {
        for (int i = 0/*deklarasi*(1) (Opsional)*/;i<5/*kondisi perulangan akan dilanjutkan(2)(true/false)*/; i++ /*Incremental*/){
            System.out.println("cetak : " + i);//body perulangan(3)(opsional)
        }
    }
}
