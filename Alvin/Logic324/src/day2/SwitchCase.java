package day2;

public class SwitchCase {
    public static void main(String[] args) {

        int a = 3;

        switch (a){
            case 1 :
                System.out.println("a is "+a);
                break;
            case 2 :
                System.out.println("a is "+a);
                break;
            case 3 :
                System.out.println("a is "+a);
                break;
            default:
                System.out.println("a is undefine");
        }

    }
}
