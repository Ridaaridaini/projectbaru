package mendalamiLatihanLogic;

public class LoopBasicSoal08 {

    public static void main(String[] args) {

        int n = 7;

        int deret = 3;

        for (int i = 0; i < n; i++) {

            System.out.print(deret + " ");

            deret *= 3;
        }

    }

}
