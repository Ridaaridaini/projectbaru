package day1;

public class Komparasi {
    public static void main(String[] args) {
        // equals (==)
        System.out.println(1 == 1); // true

        // not equals (!=)
        System.out.println(1 != 1); // false

        // greater than (>)
        System.out.println(1 > 1); // false
        
        // less than (<)
        System.out.println(1 < 1); // false

        // greater than or equal (>=)
        System.out.println(1 >= 1); // true

        // less than or equal (<=)
        System.out.println(1 <= 1); // true
    }
}
